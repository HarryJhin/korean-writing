#!/usr/bin/env node
// SessionStart 훅: 세션 시작(startup/resume/clear/compact)마다 R1(S1) 규칙을
// 컨텍스트에 주입한다. 응답을 사후 차단하지 않고, 애초에 위반을 안 쓰도록 사전 유도한다.
// stdout이 그대로 컨텍스트에 실린다(공식 docs: SessionStart, "stdout is added as context").
// R1 목록은 규칙 SoT인 S1_PATTERNS에서 파생해 동기화 지점을 늘리지 않는다.
import { S1_PATTERNS } from '../lib/prose-checks.js'

// 이름을 백틱으로 감싼다. 이중 피동 이름엔 예시 리터럴(되어진다 등)이 들어 있어,
// stripCode가 인라인 코드로 제외해야 주입 텍스트가 자기 S1을 준수한다(repo 관례).
const forbidden = S1_PATTERNS.map(p => `- \`${p.name}\``).join('\n')

// 비-S1 최상위 신호. 결정론이 아니라 큐레이션한 소수다. 주입은 오탐을 못 내니(게이트가
// 아니다) S1을 넘어 방어해도 안전하다. 규칙 본문 R2·R4와 S2_MARKERS에 대응하므로,
// 이 목록을 고칠 땐 그쪽도 함께 본다(파일 게이트는 여전히 S1만 검사한다).
process.stdout.write(
  `[korean-writing] 한국어로 글을 쓸 때 다음 S1 패턴은 코드 블록 밖 본문에 쓰지 않는다. ` +
  `한 번만 나와도 AI 티가 확정되는 패턴이다.\n${forbidden}\n` +
  `대신 이렇게 쓴다. em dash는 쉼표·괄호·문장 분리로, 이모지는 텍스트로, ` +
  `세미콜론은 마침표로 문장을 나눠서, 이중 피동은 능동이나 단순 피동(된다·보인다·잊힌다·쓰인다)으로.\n\n` +
  `빈발하는 비-S1 AI 티도 피한다. 결정론은 아니라 상황을 보되 다음을 특히 조심한다.\n` +
  `- 연결어미(-고·-며·-지만·-면서·-아서·-어서·-는데) 뒤 쉼표. 한국어 LLM의 최강 단일 신호다.\n` +
  `- "에 의해·통해·대해" 남용. 조사와 동사로 푼다.\n` +
  `- "할 수 있다" 남발. 단정형으로 바꾼다.\n` +
  `- "결론적으로·요약하자면" 류 결산 상투구.\n` +
  `번역투·만연체·AI슬롭의 깊은 규칙은 writing-korean 스킬(R2~R7)을 따른다.\n`,
)
